export function getCurrentLanguage() {
    // First check if we have a language in localStorage
    const savedLang = localStorage.getItem('lang');
    if (savedLang) {
        return savedLang;
    }

    // If no language is set, try to detect browser language
    const browserLang = navigator.language || navigator.userLanguage;
    if (browserLang) {
        const shortLang = browserLang.split('-')[0];
        if (['es', 'en', 'fr'].includes(shortLang)) {
            localStorage.setItem('lang', shortLang);
            return shortLang;
        }
    }

    // Default to Spanish if no language is set or detected
    localStorage.setItem('lang', 'en');
    return 'en';
}

